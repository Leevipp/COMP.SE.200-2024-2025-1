import upperFirst from "../src/upperFirst.js";

describe("tests for upperFirst", () => {
    test("return given string with capitalized first letter", () => {
        expect(upperFirst("hello")).toBe("Hello");
        expect(upperFirst("world")).toBe("World");
    });

    test("return given string as is if all letters are already uppercase", () => {
        expect(upperFirst("HELLO")).toBe("HELLO");
        expect(upperFirst("WORLD")).toBe("WORLD");
    });

    test("return empty string when given an empty string", () => {
        expect(upperFirst("")).toBe("");
    });

    test("return empty string when given null", () => {
        expect(upperFirst(null)).toBe("");
    });

    test("return given string as is if the input contains numbers", () => {
        expect(upperFirst("123helloworld")).toBe("123helloworld");
    });

    test("throw error if input is not a string", () => {
        expect(() => upperFirst(123)).toThrow(TypeError);
    });
});

